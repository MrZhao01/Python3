# 定义默认画笔秒针形状
goto(-40, -2)
color('red', 'gold')
up()
begin_poly()
goto(150, -2)
goto(165, 0)
goto(150, 2)
goto(-40, 2)
end_poly()
p = get_poly()
register_shape('clocksec', p)
shape('clocksec')
home()
# 定义画笔2分针形状
pen2 = Turtle()
